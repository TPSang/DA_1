    void fill() {
        List<DonHangChiTiet> list = dao.XemChiTiet(ThongKe.Month,ThongKe.year);
        int stt=1;
        for (DonHangChiTiet dh : list) {
            Object[] row={
                stt,
                dh.getMadh(),
                dh.getNgaydat(),
                dh.getTongtien()      
            };
            model.addRow(row);
            stt++;
        }
        tblDT.setModel(model);
    }

